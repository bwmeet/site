<h1>Our code of conduct</h1>
<p>We’re nice people. We attract nice people. We’re committed to providing a safe space for people to interact in. We try our best to make people feel welcome and as far as we’re aware have had nothing but positive experiences.</p>
<p>The purpose of this code of conduct is to maintain this atmosphere going forward.</p>
<p>To do this both the organisers and participants promise:</p>
<ul>
    <li>Not to use threatening language, both body language or verbally.</li>
    <li>Not to harass</li>
    <li>Not to use sexual language or imagery</li>
    <li>Not to discriminate based on gender, age, sexual orientation, disability, physical appearance, body size, race, ethnicity, religion, or technology choices.</li>
</ul>
<p>If you feel that we're crossing any of these then please let us know as it will never be intentional.</p>
<p>If you feel that someone is violating any of these, either towards yourself or someone else, contact Tim, Francesca, or a member of staff.</p>