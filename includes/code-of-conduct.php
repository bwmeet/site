<h1>Our code of conduct</h1>
<div class="content-extended">    
    <p>We’re nice people. We attract nice people. Because of that, we’re committed to providing a safe space for people to interact in. We try our best to make people feel welcome. As far as we’re aware have had nothing but positive experiences so far.</p>
    <p>The purpose of this code of conduct is to maintain this atmosphere.</p>
    <p>To do this, both the organisers and participants promise:</p>
    <ul>
        <li>Not to use threatening verbal or body language.</li>
        <li>Not to harass.</li>
        <li>Not to use sexual language or imagery.</li>
        <li>Not to discriminate based on gender, age, sexual orientation, disability, physical appearance, body size, race, ethnicity, religion, or technology choices.</li>
    </ul>
    <p>If you feel that someone is violating any of these, either towards yourself or someone else, contact Tim, Francesca, or a member of staff. If you feel that the organisers are crossing any of these then please let us know as it will <em>never</em> be intentional.</p>
</div>