<h1>About B &amp; W Meet</h1>
<img src="/images/promo.jpg" alt="B &amp; W Meet in full swing." />
<p>B &amp; W Meet is an informal pub meet for Bournemouth&ndash;based digital creatives.</p>
<p>Don’t worry if you’ve not been to an event like this before, it’s very laid back and we’ll make sure that you’re not alone for long. We’re all a bit awkward but that’s what makes it fun.</p>
<p>It doesn’t matter where you’re from or how experienced you are as long as you’re passionate about the web.</p>
<p>We’ll always be free and open to all and we look forward to meeting or seeing you again!</p>